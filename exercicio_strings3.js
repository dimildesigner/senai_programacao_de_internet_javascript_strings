// Declare a variável nome com o valor "Ana Clara". 
// Substitua o nome "Ana" por "Maria" 
// usando o método .replace() e exiba o resultado.

let nome = "Ana Clara";
let nomeAlterado = nome.replace("Ana", "Maria");

console.log("\nO nome era: " + nome + "..."); // "Ana Clara"

console.log("e passou a se chamar: " + nomeAlterado + ".\n"); // "Maria Clara"